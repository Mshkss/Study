#1 Реализация и сравнение алгоритмов линейного и двоичного поиска элемента в массиве.

def linear_search(arr, target):
    for i in range(len(arr)):
        if arr[i] == target:
            return i  # Возвращает индекс найденного элемента
    return -1  # Если элемент не найден


def binary_search(arr, target):
    left, right = 0, len(arr) - 1
    while left <= right:
        mid = (left + right) // 2
        if arr[mid] == target:
            return mid
        elif arr[mid] < target:
            left = mid + 1
        else:
            right = mid - 1
    return -1  # Если элемент не найден

#2 Алгоритм поиска максимального элемента. Начальное значение для переменной, хранящей максимум.

def find_max(arr):
    if len(arr) == 0:
        return None  # Обработка пустого массива
    max_value = arr[0]  # Начальное значение для максимума
    for num in arr:
        if num > max_value:
            max_value = num
    return max_value

